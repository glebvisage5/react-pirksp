/**
 * PDF Export Utility for TeamHub Specifications
 * Generates PDF documents from specification blocks
 */

interface Block {
  id: string;
  type: string;
  content: any;
  order: number;
}

interface SpecData {
  title: string;
  project?: string;
  version?: string;
  author?: string;
  team?: string;
  createdAt?: string;
  blocks: Block[];
}

