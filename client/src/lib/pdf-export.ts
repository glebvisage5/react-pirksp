/**
 * Render individual block to HTML
 */
function renderBlock(block: Block): string {
  switch (block.type) {
    case 'heading':
      const level = block.content.level || 2;
      return `<div class="block"><h${level}>${escapeHTML(block.content.text)}</h${level}></div>`;

    case 'text':
      return `<div class="block"><p>${escapeHTML(block.content.text).replace(/\n/g, '<br>')}</p></div>`;

    case 'list':
      const listTag = block.content.ordered ? 'ol' : 'ul';
      const items = block.content.items.map((item: string) => `<li>${escapeHTML(item)}</li>`).join('\n');
      return `<div class="block"><${listTag}>${items}</${listTag}></div>`;

    case 'table':
      const headers = block.content.headers.map((h: string) => `<th>${escapeHTML(h)}</th>`).join('');
      const rows = block.content.rows
        .map((row: string[]) => {
          const cells = row.map((cell: string) => `<td>${escapeHTML(cell)}</td>`).join('');
          return `<tr>${cells}</tr>`;
        })
        .join('\n');
      return `
        <div class="block">
          <table>
            <thead><tr>${headers}</tr></thead>
            <tbody>${rows}</tbody>
          </table>
        </div>
      `;

    case 'image':
      return `
        <div class="block">
          <img src="${escapeHTML(block.content.url)}" alt="${escapeHTML(block.content.alt)}" />
        </div>
      `;

    case 'code':
      return `
        <div class="block">
          <pre><code>${escapeHTML(block.content.code)}</code></pre>
        </div>
      `;

    case 'link':
      return `
        <div class="block">
          <a href="${escapeHTML(block.content.url)}" class="link" target="_blank">
            🔗 ${escapeHTML(block.content.text)}
          </a>
        </div>
      `;

    case 'checklist':
      const checklistItems = block.content.items
        .map((item: any) => {
          const checked = item.checked ? 'checked' : '';
          const checkedClass = item.checked ? 'checked' : '';
          return `
            <div class="checklist-item ${checkedClass}">
              <input type="checkbox" ${checked} disabled />
              <span class="checklist-text">${escapeHTML(item.text)}</span>
            </div>
          `;
        })
        .join('\n');
      return `<div class="block"><div class="checklist">${checklistItems}</div></div>`;

    default:
      return '';
  }
}

/**
 * Escape HTML special characters
 */
function escapeHTML(str: string): string {
  if (!str) return '';
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

/**
 * Export specification as PDF (opens in new window for printing/saving)
 */
export function exportSpecToPDF(spec: SpecData, language: 'en' | 'ru' = 'en'): void {
  const html = generateSpecHTML(spec, language);
  
  // Open in new window
  const printWindow = window.open('', '_blank');
  if (printWindow) {
    printWindow.document.write(html);
    printWindow.document.close();
    
    // Wait for content to load, then trigger print dialog
    printWindow.onload = () => {
      setTimeout(() => {
        printWindow.print();
      }, 250);
    };
  }
}

/**
 * Download specification as HTML file
 */
export function downloadSpecAsHTML(spec: SpecData, language: 'en' | 'ru' = 'en'): void {
  const html = generateSpecHTML(spec, language);
  const blob = new Blob([html], { type: 'text/html' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `${spec.title.replace(/[^a-z0-9]/gi, '_').toLowerCase()}.html`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
